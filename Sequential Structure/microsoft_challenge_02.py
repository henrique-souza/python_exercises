first_number = int(input("First number: "))
second_number = int(input("\nSecond Number: "))

soma = first_number + second_number
subtracao = first_number - second_number
multiplicacao = first_number * second_number
divisao = first_number / second_number
exponenciacao = first_number ** second_number
resto_de_divisao = first_number % second_number

print("\n\nResultado da Soma: " + str(soma))
print("\nResultado da Subtração: " + str(subtracao))
print("\nResultado da Multiplicação: " + str(multiplicacao))
print("\nResultado da Divisão: " + str(divisao))
print("\nResultado da Exponenciação: " + str(exponenciacao))
print("\nResultado do resto da Divisão: " + str(resto_de_divisao))
